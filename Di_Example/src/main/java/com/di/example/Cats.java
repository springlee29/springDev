package com.di.example;

public class Cats {

	public void catsName(String firstCatName, String secondCatName) {
		System.out.println("catName()");
		System.out.println("첫번째 이름 "+ firstCatName);
		System.out.println("두번째 이름 " + secondCatName);
	}
	public void catsAge(int firstCatAge, int secondCatAge) {
		System.out.println("catAge()");
		System.out.println("첫번째 나이 "+ firstCatAge);
		System.out.println("두번째 나이 " + secondCatAge);
		
	}
}
